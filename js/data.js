// وظائف مساعدة
function getLessonById(id) {
    return lessonsData.find(l => l.id === id);
}

function getLessonsByModule(moduleId) {
    return lessonsData.filter(l => l.moduleId === moduleId).sort((a, b) => a.order - b.order);
}

function getModuleById(id) {
    return modulesData.find(m => m.id === id);
}

function getNextLesson(currentLessonId) {
    const current = getLessonById(currentLessonId);
    if (!current) return null;
    const next = lessonsData.find(l => l.order === current.order + 1);
    return next || null;
}

function getPreviousLesson(currentLessonId) {
    const current = getLessonById(currentLessonId);
    if (!current) return null;
    const prev = lessonsData.find(l => l.order === current.order - 1);
    return prev || null;
}